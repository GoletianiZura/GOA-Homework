# for loop'ით და while loop'ით ეცადეთ გამოიტანოთ რიცხვები 1'დან 100-ის ჩათვლით
for i in range(100):
    print(i + 1)
print("-----")

i = 0
while i <= 100:
    print(i)
    i = i + 1
print("-----")


# for ციკლის მეშვეობით შეკრიბეთ პირველი 10 რიცხვი

sum_of_digits = 0
for i in range(11):
    sum_of_digits += i
print(sum_of_digits)


print("-----")

# for ციკლის მეშვეობით გამოიტანეთ ლუწი რიცხვები 1'დან 20'ის ჩათვლით

for i in range(20):
    print(i + 1)
    i = i + 1
sum_of_digits = 0
